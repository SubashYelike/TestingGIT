package JavaPrograms;

public class IfCondition {

	public static void main(String[] args) {

		int A = 10;
		int B = 20;
		int C = 5;

		if (A < B) {

			System.out.println("Yes A smaller than B");
		}

		if (A > C) {

			System.out.println("Yes A is grater than C");
		}

		if (B > C) {

			System.out.println("Yes B greater than C");
		}
	}

}
